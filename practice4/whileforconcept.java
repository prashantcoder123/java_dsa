class whileforconcept{
    public static void main(String args[]){
        int i=0,j=10;
        while(i<j){
            System.out.println(i + " "+ j);
            i++;
            j--;
        }
        for(int k =0,l =10; k<j;k++,l--){
            System.out.println(k +" "+ l);
        }
    }
} 