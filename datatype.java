public class datatype{
    public static void main(String args[]){
        byte b =8;        // 1byte =8bit..
        System.out.println(b);
        char ch ='a';   //character 
        System.out.println(ch);
        boolean var = true;  //true or false 
        System.out.println(var);
        // float pencil = (float) 1.5;     // float value.
        float pencil = 1.5f;           // ye aur upar bala me se koi ek lenge float value.
        System.out.println(pencil);
        int number =25;      //int type..
        System.out.println(number);
        double n =123.433;   //large floating  ..
        System.out.println(n);
        long n1 =123433;     //large number
        System.out.println(n1);
        short s=240;       // 16 bit signed...
        System.out.println(s);
         

         
    }
}